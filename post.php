          INTRODUCTION <br />
          Anger is a natural phenomenon and so comes naturally when the heart is
          provoked. We cannot stop it from coming and that is why we are
          permitted to be angry as seen in our text above. As Christians, we are
          instructed to exercise self control in all things and that includes
          over anger. Proverbs 15:8 <br />A wrathful man stirreth up strife:
          But he that is slow to Anger appeaseth strife.<br /><br />
          Proverbs 16:32<br />
          He that is slow to Anger is better than the mighty; And he that ruleth
          his spirit than he that taketh a city. <br />It is because anger is
          manageable that we are given additional instructions not to sin when
          angry or give place to the devil. (Eph. 4:26-27) This means that we
          have so many options when we are angry: either to obey God’s command
          on anger or behave like people that don’t have or know God. Whatever
          we choose to do when angry shows our true identity because not all who
          profess God knows God. <br /><br />WHY WE MUST MANAGE ANGER
          <br />Anger in the heart of a man is like a volcano in the ground
          which is extremely hot and gaseous and waiting to erupt. If let out,
          it devours everything on its way. The difference between volcano and
          anger is that volcano is natural and so not regulated. In the case of
          anger, its eruption or otherwise is decided by the man in whose heart
          it burns. That is why God wants us to manage anger. We therefore
          manage anger for the following reasons:<br /><br />
          Because anger makes us vulnerable to sin We are vulnerable to sin when
          we are angry. This is because, nothing good comes from anger. All that
          we do or say out of anger are never in tandem with the spirit of
          holiness. <br />James 1:19-20 19<br />
          Wherefore, my beloved brethren, let every man be swift to hear, slow
          to speak, slow to wrath:  20 for the wrath of man worketh not
          the righteousness of God. Those that have conscience usually live to
          regret most of the things they did or said when they were angry but
          some without conscience don’t border.
        </p>
        <p>
          Because a child of God must avoid sin at all cost The problem is that
          some Christians think that what constitute sin are only such things
          like fornication or adultery, stealing, lying, worshipping idols, etc.
          They don’t count malicious words, evil speaking, grudge, etc as sins.
          Those are such things that usually come from anger. The real children
          of God must consciously avoid sins that come from anger.<br /><br />
          1 John 5:18-20 18 <br />We know that whosoever is born of God sinneth
          not; but he that is begotten of God keepeth himself, and that wicked
          one toucheth him not...
        </p>
